const timer = (id, deadline) => {


  function getTimeRemaing(endtime) {
    let days, hours, minutes, seconds;
    const t = Date.parse(endtime) - Date.parse(new Date());

    if (t <= 0) {
      days = 0;
      hours = 0;
      minutes = 0;
      seconds = 0;
    } else {
      seconds = Math.floor((t / 1000) % 60),
        minutes = Math.floor((t / 1000 / 60) % 60),
        hours = Math.floor((t / (1000 * 60 * 60)) % 24),
        days = Math.floor(t / (1000 * 60 * 60 * 24));
    }

    return {
      'total': t,
      'days': days,
      'hours': hours,
      'minutes': minutes,
      'seconds': seconds
    };
  }

  function getZero(num) {
    if (num >= 0 && num < 10) {
      return `0${num}`;
    } else {
      return num
    }
  }

  const months = [
    "января",
    "февраля",
    "марта",
    "апреля",
    "мая",
    "июня",
    "июля",
    "августа",
    "сентября",
    "октября",
    "ноября",
    "декабря",
  ];

  function setClock(selector, endtime) {
    const timer = document.querySelector(selector),
      days = timer.querySelector('#days'),
      hours = timer.querySelector('#hours'),
      minutes = timer.querySelector('#minutes'),
      seconds = timer.querySelector('#seconds'),
      timeInterval = setInterval(updateClock, 1000);

    updateClock();

    function updateClock() {
      let text = document.querySelector(".sale_subtitle");
      let finalTextTimerStop = document.querySelector(".timer1 h4");
      const t = getTimeRemaing(endtime);
      days.innerHTML = getZero(t.days);
      hours.innerHTML = getZero(t.hours);
      minutes.innerHTML = getZero(t.minutes);
      seconds.innerHTML = getZero(t.seconds);

      if (t.total <= 0) {
        clearInterval(timeInterval);
        text.innerHTML = `Акция закончилась <span>
          ${new Date(endtime).getDate()} 
          ${months[new Date(endtime).getMonth()]}</span>!`;
        finalTextTimerStop.innerHTML = `Акция закончилась`

      } else {
        text.innerHTML = `Успей сэкономить на остеклении! Только до <span>
          ${new Date(endtime).getDate()} 
          ${months[new Date(endtime).getMonth()]}</span>!`;
      }
    }


  }

  setClock(id, deadline)
};

export default timer;